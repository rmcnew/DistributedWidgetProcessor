import logging

def init(filename):
    logging.basicConfig(format='%(asctime)s [%(levelname)s] %(filename)s:%(lineno)d : %(message)s',
                        filename=f"{filename}.log",
                        filemode="w",
                        level=logging.INFO)
    logging.info(f"{filename} starting up")

